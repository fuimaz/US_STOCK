"""
Generate K-line charts for symbols that have MA Convergence signals in a scan CSV,
and enrich a summary table with extra stock info (name, market cap, daily pct change).

Inputs:
1) scan CSV generated by scanners/scan_ma_convergence_daily.py
2) local daily cache: data_cache/{symbol}_{years}y_1d_forward.csv

Outputs:
- PNG charts (candles + volume + MA + buy/sell markers)
- charts.csv (enriched)
- index.html (preview)

Example:
    python scripts/generate_ma_convergence_charts_from_scan.py ^
      --scan-csv results/ma_convergence_daily_scan_mv100yi_20260302_144411/ma_convergence_scan_20260302_145754.csv ^
      --data-years 20 --plot-years 5 --workers 4
"""

